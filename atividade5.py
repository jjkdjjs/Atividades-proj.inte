print("No programa será mostrado a soma, subtração e multiplicação de dois valores\n")
valorA = float(input("Digite o primeiro valor: "))
valorB = float(input("Digite o segundo valor: "))

print("A soma dos dois valores é:", valorA + valorB)
print("A substração dos dois valores é:", valorA - valorB)
print("A multiplicação dos dois valores é:", valorA * valorB)